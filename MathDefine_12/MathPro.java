package MasterOOPBestJava.MathDefine_12;

public class MathPro {

    public static void main(String args []){

         // all Math properties are define to class..
        System.out.println(Math.abs(8));
        System.out.println(Math.sqrt(25));
        System.out.println(Math.asin(20.50));
        System.out.println(Math.max(10,8));
        System.out.println(Math.min(10,8));
        System.out.println(Math.acos(20.36));
        System.out.println(Math.addExact(10,50));
        System.out.println(Math.ceil(10.50));
        System.out.println(Math.cbrt(10.20));
        System.out.println(Math.PI);
        System.out.println(Math.random());
        System.out.println(Math.tan(20.10));
        System.out.println(Math.pow(5,2));
        System.out.println(Math.multiplyExact(2,2));
        System.out.println(Math.log10(20.55));
        System.out.println(Math.toRadians(5.6));
        System.out.println(Math.nextUp(4.5));
        System.out.println(Math.sin(0));
        System.out.println(Math.tan(90));

    }
}
